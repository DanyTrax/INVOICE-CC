<h2 class="text-2xl font-semibold text-gray-900 mb-6">Términos y Condiciones del Servicio</h2>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">1. Objeto y aceptación</h3>
    <p>Los presentes términos y condiciones («Términos») regulan el uso del sistema RAMS (Regulatory Affairs Management System) y de los servicios asociados ofrecidos por <strong>Asesoría y Consultoría Doble Vía</strong> («Doble Vía», «nosotros»). El acceso y uso del sistema (incluido el dominio acdoblevia.com / regulatory.acdoblevia.com) implica la aceptación de estos Términos. Si actúa en nombre de una empresa o organización, declara tener capacidad para vincularla.</p>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">2. Descripción del servicio</h3>
    <p>RAMS es una plataforma de gestión regulatoria destinada a empresas privadas y sus usuarios autorizados. Permite, entre otras funciones: gestión de clientes (empresas), cotizaciones, expedientes o procesos regulatorios, checklist documental, subida y organización de documentos en Google Drive, y flujos de radicación y seguimiento ante autoridades. El servicio se presta en la modalidad y alcance acordados con Doble Vía.</p>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">3. Uso aceptable</h3>
    <p>El usuario se compromete a:</p>
    <ul class="list-disc pl-6 mt-2 space-y-1">
        <li>Utilizar el sistema solo para fines legítimos relacionados con la gestión regulatoria y la relación comercial con Doble Vía.</li>
        <li>Mantener la confidencialidad de sus credenciales y no ceder el acceso a terceros no autorizados.</li>
        <li>No utilizar el sistema para actividades ilícitas, fraudulentas o que vulneren derechos de terceros.</li>
        <li>Proporcionar información veraz y actualizada cuando sea requerida para el servicio.</li>
    </ul>
    <p class="mt-2">La autorización de acceso a Google Drive se utilizará únicamente para crear y organizar carpetas y archivos vinculados a los expedientes gestionados en RAMS, según la configuración establecida.</p>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">4. Propiedad intelectual y datos</h3>
    <p>El software RAMS, su diseño y los elementos propios de la plataforma son propiedad de Doble Vía o de sus licenciantes. Los datos e información que el usuario introduzca o suba (incluidos los documentos en Drive asociados al servicio) siguen siendo del usuario o de la organización que representa; Doble Vía los trata únicamente para la prestación del servicio y de acuerdo con la Política de Privacidad.</p>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">5. Disponibilidad y modificaciones</h3>
    <p>Doble Vía se esfuerza por mantener el servicio disponible, pero no garantiza ausencia de interrupciones. Podemos realizar mantenimientos, actualizaciones o cambios en las funcionalidades, notificando cuando sea razonable. El uso continuado del sistema tras dichos cambios constituye aceptación de las nuevas condiciones aplicables.</p>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">6. Limitación de responsabilidad</h3>
    <p>En la medida permitida por la ley aplicable, Doble Vía no será responsable por daños indirectos, consecuentes o lucro cesante derivados del uso o la imposibilidad de uso del sistema. La responsabilidad por decisiones tomadas a partir de la información gestionada en RAMS recae en el usuario y la organización que utilice el servicio.</p>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">7. Suspensión y terminación</h3>
    <p>Doble Vía podrá suspender o dar por terminado el acceso al sistema en caso de incumplimiento de estos Términos, por decisión comercial o por requisitos legales. El usuario podrá solicitar la baja del servicio según los acuerdos establecidos con Doble Vía.</p>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">8. Ley aplicable y contacto</h3>
    <p>Estos Términos se rigen por la ley aplicable en la jurisdicción en la que Doble Vía ejerza su actividad. Para cualquier consulta en relación con estos Términos o con el servicio, el usuario puede contactar a Doble Vía a través de los canales oficiales proporcionados (correo electrónico o medio indicado en la relación contractual).</p>
</section>
