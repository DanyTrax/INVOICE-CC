<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">1. Responsable del tratamiento</h3>
    <p>El responsable del tratamiento de los datos personales es <strong>Asesoría y Consultoría Doble Vía</strong> (en adelante, «Doble Vía»), empresa privada que ofrece servicios de asesoría y consultoría regulatoria. Los datos se procesan a través del sistema RAMS (Regulatory Affairs Management System) y del dominio acdoblevia.com / regulatory.acdoblevia.com.</p>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">2. Finalidad del tratamiento</h3>
    <p>Los datos personales y de negocio se recaban y procesan para:</p>
    <ul class="list-disc pl-6 mt-2 space-y-1">
        <li>Gestionar usuarios, empresas (clientes) y expedientes o procesos regulatorios.</li>
        <li>Gestionar cotizaciones, documentos y comunicaciones relacionadas con los servicios de asesoría regulatoria.</li>
        <li>Integrar con Google Drive para crear carpetas y subir documentos de los expedientes, de forma organizada por cliente y por país, según la configuración del sistema.</li>
        <li>Enviar notificaciones por correo electrónico cuando la configuración del sistema lo permita (por ejemplo, invitaciones o avisos del sistema).</li>
    </ul>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">3. Datos que recogemos</h3>
    <p>Podemos tratar, entre otros:</p>
    <ul class="list-disc pl-6 mt-2 space-y-1">
        <li><strong>Datos de cuenta:</strong> nombre de usuario, correo electrónico y contraseña (almacenada de forma segura).</li>
        <li><strong>Datos de empresas/clientes:</strong> nombre, NIT/RUT, dirección, país, teléfono, persona de contacto y correo de contacto.</li>
        <li><strong>Datos de expedientes y documentos:</strong> información de cotizaciones, procesos regulatorios, nombres de documentos y archivos que se suben al sistema y a Google Drive (vinculado a la cuenta de Google que autorice la organización).</li>
        <li><strong>Registros de actividad:</strong> acciones realizadas en el sistema para fines de auditoría y soporte.</li>
    </ul>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">4. Uso de Google Drive</h3>
    <p>El sistema puede solicitar acceso a Google Drive para crear carpetas y subir o organizar archivos en nombre de la organización. El acceso se utiliza únicamente para esta finalidad operativa (gestión documental de expedientes). Los datos que se suben a Drive quedan bajo la cuenta o unidad que la organización configure; Doble Vía no utiliza estos datos para fines distintos a la prestación del servicio.</p>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">5. Base legal y conservación</h3>
    <p>El tratamiento se basa en la ejecución del contrato o relación comercial con Doble Vía y, cuando aplique, en el consentimiento (por ejemplo, al autorizar el acceso a Google Drive). Los datos se conservan mientras sea necesario para la prestación del servicio y el cumplimiento de obligaciones legales; después pueden ser anonimizados o eliminados según la política interna.</p>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">6. Seguridad</h3>
    <p>Se aplican medidas técnicas y organizativas para proteger los datos frente a accesos no autorizados, pérdida o alteración. El acceso al sistema está restringido a usuarios autorizados y las contraseñas se almacenan de forma segura.</p>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">7. Derechos</h3>
    <p>Usted puede ejercer los derechos de acceso, rectificación, supresión, limitación del tratamiento, portabilidad y oposición, así como reclamar ante la autoridad de protección de datos que corresponda. Para ejercer estos derechos o formular consultas sobre privacidad, contacte a Doble Vía a través del correo electrónico o canal que se le haya indicado como contacto oficial (por ejemplo, el correo de la empresa o el de soporte del sistema).</p>
</section>

<section>
    <h3 class="text-lg font-medium text-gray-900 mb-2">8. Cambios</h3>
    <p>Doble Vía puede actualizar esta política de privacidad. La fecha de última actualización se indica al inicio del documento. El uso continuado del sistema tras cambios relevantes implica la aceptación de la política actualizada.</p>
</section>
